public class StringExample {
    public static void main(String[] args) {
        // 문자열
        System.out.println("I am String");
        // 문자
        System.out.println('I');
        // back slash
        System.out.println("안녕 나는 \"자바\"야.");
        // 줄 바꿈
        System.out.println("치킨은 살 안 쪄요. \n살은 내가 쩌요.");
    }
}
