public class AgeOfGildong {
    public static void main(String[] args) {

        // 연습 문제1
        int ageGildong = 8;
        int ageAfter10Ys = ageGildong + 10;
        int ageAfter20Ys = ageGildong + 20;
        int ageAfter30Ys = ageGildong + 30;

        System.out.println(ageAfter10Ys);
        System.out.println(ageAfter20Ys);
        System.out.println(ageAfter30Ys);
        System.out.println('\n');

        // 연습 문제2
        ageGildong = 9;
        ageAfter10Ys = ageGildong + 10;
        ageAfter20Ys = ageGildong + 20;
        ageAfter30Ys = ageGildong + 30;

        System.out.println(ageAfter10Ys);
        System.out.println(ageAfter20Ys);
        System.out.println(ageAfter30Ys);

    }
}
